async function seedMore() {
  await initDb();

  // 检查现有单词数
  const count = get('SELECT COUNT(*) as cnt FROM words');
  const existing = count ? count.cnt : 0;

  if (existing >= 3000) {
    console.log(`已有 ${existing} 个单词，足够接近 3500 词标准。`);
    return;
  }

  console.log(`当前单词数: ${existing}，需要补充 ${extraWords.length} 个...`);

  let success = 0;
  for (const w of extraWords) {
    try {
      // 检查是否已存在
      const existingWord = get('SELECT id FROM words WHERE word = ?', [w[0]]);
      if (existingWord) {
        continue; // 已存在，跳过
      }
      run(
        'INSERT INTO words (word, translation, phonetic, example_sentence, example_translation, difficulty, list_type) VALUES (?, ?, ?, ?, ?, ?, ?)',
        [w[0], w[1], '', '', '', w[2] || 2, 'gaokao']
      );
      success++;
    } catch (err) {
      // 忽略重复
    }
  }

  const total = get('SELECT COUNT(*) as cnt FROM words');
  console.log(`✅ 补充完成！新增 ${success} 个单词`);
  console.log(`   总计 ${total ? total.cnt : 0} 个单词`);
  console.log(`   数据库位置: D:/word-remember/data.db`);
}

seedMore().catch(err => {
  console.error('补充导入失败:', err);
  process.exit(1);
});
